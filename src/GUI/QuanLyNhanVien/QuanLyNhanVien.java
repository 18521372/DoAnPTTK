    private void deleteButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteButtonMouseClicked
        if (tableSalary.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhân viên muốn xóa!");
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc sẽ xóa nhân viên này chứ?");
            if (confirm == JOptionPane.OK_OPTION) {
                NhanVien nhanVien = this.getModel();
                nhanVienController.delete(nhanVien);

                this.removeAllElementInTable();
                this.displayDataForTableSalary();
                JOptionPane.showMessageDialog(null, "Xóa thành công!");
            }
        }
    }//GEN-LAST:event_deleteButtonMouseClicked

    private void buttonSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonSearchMouseClicked
        String searchString = textSearch.getText().toLowerCase().trim();
        ArrayList<NhanVien> list = nhanVienController.getAllNhanVien();
        this.removeAllElementInTable();

        for (int i = 0; i < list.size(); i++) {
            NhanVien nhanVien = list.get(i);
            String maNhanVien = "" + nhanVien.getMaNhanVien();
            String tenNhanVien = nhanVien.getTenNhanVien().toLowerCase().trim();
            if (maNhanVien.equals(searchString) || tenNhanVien.contains(searchString)) {
                Vector row = new Vector();
                row.add(i + 1);
                row.add(nhanVien.getMaNhanVien());
                row.add(nhanVien.getTenNhanVien());
                row.add(nhanVien.getGioiTinh() == 1 ? "Nam" : "Nu");
                row.add(nhanVien.getHeSoLuong());
                row.add(nhanVien.getLuongThuong());
                row.add(nhanVien.getLuongCoBan() * nhanVien.getHeSoLuong() + nhanVien.getLuongThuong());
                row.add(nhanVien.getGhiChu());

                String trangThai = "";
                switch (nhanVien.getStatusFlag()) {
                    case 0:
                        trangThai = "Nghỉ việc";
                        break;
                    case 1:
                        trangThai = "Đang làm việc";
                        break;
                    default:
                        trangThai = "Nghỉ phép";
                }
                row.add(trangThai);
                //add this row to table
                DefaultTableModel defaultTableModel = (DefaultTableModel) tableSalary.getModel();
                defaultTableModel.addRow(row);
            }
        }
    }//GEN-LAST:event_buttonSearchMouseClicked

