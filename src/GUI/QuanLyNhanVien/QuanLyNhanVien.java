    private void panelSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelSaveMouseClicked
        if (!this.isEmptyField() && this.isValidDate()) {
            int result = JOptionPane.showConfirmDialog(null, "Lưu thay đổi?");
            if (result == JOptionPane.YES_OPTION) {
                NhanVien nhanVien = this.getModel();
                switch (this.state) {
                    case "Add":
                        if (nhanVienController.insert(nhanVien)) {
                            //After add new NhanVien, enable table, set all to uneditable
                            this.displayDataForTableSalary();
                            tableSalary.setEnabled(true);
                            this.disableAll();
                            this.invisibleButton();
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Something went wrong :(");
                        }
                        ;
                        break;
                    case "Edit":
                        if (nhanVienController.update(nhanVien)) {
                            //After edit, enable table, set all to uneditable
                            this.displayDataForTableSalary();
                            tableSalary.setEnabled(true);
                            this.disableAll();
                            this.invisibleButton();
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Something went wrong :(");
                        }
                }
            }
        } else if (this.isEmptyField()) {
            JOptionPane.showMessageDialog(null, "Tên hoặc số điện thoại không được trống!");
        } else {
            JOptionPane.showMessageDialog(null, "Năm sinh không hợp lệ");
        }
    }//GEN-LAST:event_panelSaveMouseClicked

