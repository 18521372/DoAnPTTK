    public void displayDataForTableSalary() {
        this.removeAllElementInTable();
        this.listNhanVien = nhanVienController.getAllNhanVien();
        ArrayList<NhanVien> list = this.listNhanVien;
        if (this.listNhanVien != null) {
            DefaultTableModel defaultTableModel = (DefaultTableModel) tableSalary.getModel();
            for (int i = 0; i < this.listNhanVien.size(); i++) {
                NhanVien nhanVien = this.listNhanVien.get(i);

                //create a row and add data
                Vector row = new Vector();
                row.add(i + 1);
                row.add(nhanVien.getMaNhanVien());
                row.add(nhanVien.getTenNhanVien());
                row.add(nhanVien.getGioiTinh() == 1 ? "Nam" : "Nu");
                row.add(nhanVien.getHeSoLuong());
                row.add(nhanVien.getLuongThuong());
                row.add(nhanVien.getLuongCoBan() * nhanVien.getHeSoLuong() + nhanVien.getLuongThuong());
                row.add(nhanVien.getGhiChu());

                String trangThai = "";
                switch (nhanVien.getStatusFlag()) {
                    case 0:
                        trangThai = "Nghỉ việc";
                        break;
                    case 1:
                        trangThai = "Đang làm việc";
                        break;
                    default:
                        trangThai = "Nghỉ phép";
                }
                row.add(trangThai);
                //add this row to table
                defaultTableModel.addRow(row);
            }
        }
    }

    private void showSelectedEmployee(int index) {
        //get selected employee
        NhanVien nhanVien = this.listNhanVien.get(index);

        //retrieve name
        textName.setText(nhanVien.getTenNhanVien());

        //retrieve gender
        if (nhanVien.getGioiTinh() == 1) {
            radioMale.setSelected(true);
        } else {
            radioFemale.setSelected(true);
        }

        //TODO: Fix this
        //retrieve date of birth
        String ngaySinh = nhanVien.getNgaySinh();
        int day = Integer.parseInt(ngaySinh.substring(8, 10));
        int month = Integer.parseInt(ngaySinh.substring(5, 7));
        int year = Integer.parseInt(ngaySinh.substring(0, 4));
        textDay.setText("" + day);
        textMonth.setText("" + month);
        textYear.setText("" + year);
        //retrieve phone
        textPhone.setText("" + nhanVien.getSoDienThoai());

        //retrieve email
        textMail.setText(nhanVien.getEmail());

        //retrieve place
        textPlace.setText(nhanVien.getQueQuan());

        //retrieve address
        textAddress.setText(nhanVien.getDiaChi());

        //retrieve position
        comboBoxPosition.setSelectedItem(nhanVien.getChucVu());

        //retrieve starting day
        String ngayVaoLam = nhanVien.getNgayVaoLam();
        textDaySalary.setText(ngayVaoLam.substring(8, 10));
        textMonthSalary.setText(ngayVaoLam.substring(5, 7));
        textYearSalary.setText(ngayVaoLam.substring(0, 4));

        //retrieve employee id
        textEmployeeId.setText("" + nhanVien.getMaNhanVien());

        //retrieve base salary
        textBaseSalary.setText("" + nhanVien.getLuongCoBan());

        //retrieve bonus salary
        textBonus.setText("" + nhanVien.getLuongThuong());

        //retrieve salary coeff
        textCoeff.setText("" + nhanVien.getHeSoLuong());

        //retrieve note
        textNote.setText(nhanVien.getGhiChu());

        //retrieve status
        switch (nhanVien.getStatusFlag()) {
            case 0:
                comboboxStatus.setSelectedItem("Nghỉ việc");
                break;
            case 1:
                comboboxStatus.setSelectedItem("Đang làm việc");
                break;
            default:
                comboboxStatus.setSelectedItem("Nghỉ phép");
        }

        float tongLuong = nhanVien.getLuongCoBan() * nhanVien.getHeSoLuong() + nhanVien.getLuongThuong();
        textSum.setText("" + tongLuong);
    }

