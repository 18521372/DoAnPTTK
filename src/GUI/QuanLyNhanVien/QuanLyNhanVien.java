    private void removeAllElementInTable() {
        DefaultTableModel model = (DefaultTableModel) tableSalary.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }

    private boolean isEmptyField() {
        boolean result;
        String name = textName.getText();
        String phone = textPhone.getText();
        //If these field is empty, return true, else return false
        result = name.equals("") && phone.equals("") ? true : false;
        return result;
    }

    private boolean isValidDate() {
        int dobDay = Integer.parseInt(textDay.getText());
        int dobMonth = Integer.parseInt(textMonth.getText());
        int dobYear = Integer.parseInt(textYear.getText());

        int startDay = Integer.parseInt(textDaySalary.getText());
        int startMonth = Integer.parseInt(textMonthSalary.getText());
        int startYear = Integer.parseInt(textYearSalary.getText());
        //If these field is valid, return true, else return false
        if ((dobDay != 0) && (startDay != 0) && (dobMonth != 0) && (startMonth != 0) && (dobYear >= 1000) && (startYear >= 1000)) {
            return true;
        } else {
            return false;
        }
    }
